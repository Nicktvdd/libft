#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;
	char	*str;

	str = (char *)s;
	i = ft_strlen(s);
	while (str[i] != c && i > 0)
	{
		i--;
	}
	str = &str[i];
	return (str);
}
