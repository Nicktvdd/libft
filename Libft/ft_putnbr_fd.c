#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	i;

	i = 0;
	if (n < 0)
	{
		ft_putchar ('-');
		if (n <= -2147483648)
		{
			ft_putchar_fd('2', fd);
			n = n % -1000000000;
		}
			n = n * -1;
	}
	if (n / 10)
	{
		ft_putnbr(n / 10);
	}
	ft_putchar_fd(n % 10 + '0', fd);
}