int	ft_strcmp(unsigned char *s1, unsigned char *s2)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && (s1[i] || s2[i]) != '\0')
	{
			i++;
	}
	return (s1[i] - s2[i]);
}
