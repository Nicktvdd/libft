#include <stdlib.h>

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	char	*d;

	d = (char *)b;
	i = 0;
	while (len > 0)
	{
		d[i++] = c;
		len--;
	}
	return (d);
}
